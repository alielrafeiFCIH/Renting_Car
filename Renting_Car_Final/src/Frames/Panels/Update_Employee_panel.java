    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        if(!IDTextField1.getText().equals("")&&!UserNameTextField1.getText().equals("")&&!NameTextField1.getText().equals("")&&!PasswordTextField1.getText().equals("")&&!PhoneNoTextField1.getText().equals("")&&!AddressTextField1.getText().equals("")&&!DOBTextField1.getText().equals("")&&!DOJTextField1.getText().equals("")){
              Employee  emp = new Employee();
              
                emp.setId(Integer.parseInt(IDTextField1.getText()));
                emp.setAddress(AddressTextField1.getText());
                emp.setName(NameTextField1.getText());
                emp.setUsername(UserNameTextField1.getText());
                emp.setPassword(PasswordTextField1.getText());
                emp.setPhone_No(PhoneNoTextField1.getText());
                emp.Set_Date_of_join(DOJTextField1.getText());
                emp.setDate_Of_Birth(DOBTextField1.getText());
                if(GenderComboBox1.getSelectedItem().equals("Male")){
                    emp.setGender("Male");
                }else if(GenderComboBox1.getSelectedItem().equals("Female")){
                    emp.setGender("Female");
                }
                if(QualificationComboBox1.getSelectedItem().equals("fresh graduate")){
                    emp.Set_qualifications("fresh graduate");
                }else if(QualificationComboBox1.getSelectedItem().equals("experience")){
                       emp.Set_qualifications("experience"); 
                }else if(QualificationComboBox1.getSelectedItem().equals("student")){
                    emp.Set_qualifications("student");
                }
                if(emp.Update_Employee(Integer.parseInt(IDTextField1.getText()), emp)){
                  ErrorLabel.setText("Uppdated Successfully");
                     ResetpanelData();
                }else{
                     ErrorLabel.setText("Failed to Add ...!");
                 }
        }else{
              ErrorLabel.setText(" Please fill the Empty Fields ....");
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed
