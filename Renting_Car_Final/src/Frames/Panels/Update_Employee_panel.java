    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        if(!SearchTextField.getText().equals("")){
            Employee emp  = new Employee();
            Employee retrived = emp.Search_For_Employee(SearchTextField.getText());
            
            if(retrived.getId()>0){
                ErrorLabel.setText("Found....");
                setPanelData(retrived);
            }else{
                ErrorLabel.setText("Not Found.......!!");
            }
        }else{
            ErrorLabel.setText(" Please fill the Search Field ....");
        }
    }//GEN-LAST:event_SearchButtonActionPerformed
    public void setPanelData(Employee emp){
        IDTextField1.setText(""+emp.getId());
        NameTextField1.setText(""+emp.getName());
        AddressTextField1.setText(""+emp.getAddress());
        UserNameTextField1.setText(""+emp.getUsername());
        PasswordTextField1.setText(""+emp.getPassword());
        PhoneNoTextField1.setText(""+emp.getPhone_No());
        DOBTextField1.setText(""+emp.getDate_Of_Birth());
        DOJTextField1.setText(""+emp.Get_Date_of_join());
        if(emp.getGender().equals("Male")){
            GenderComboBox1.setSelectedIndex(0);
        }else if(emp.getGender().equals("Female")){
            GenderComboBox1.setSelectedIndex(1);
        }
        if(emp.Get_qualification().equals("fresh graduate")){
            QualificationComboBox1.setSelectedItem("fresh graduate");
        }else if(emp.Get_qualification().equals("experience")){
            QualificationComboBox1.setSelectedItem("experience");
            
        }else if(emp.Get_qualification().equals("student")){
             QualificationComboBox1.setSelectedItem("student");
        }
    }
